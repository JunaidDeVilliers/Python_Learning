friends = 0

# Addition
friends = friends + 1   # adding 1 friend
friends += 1            # adding 1 friend
print(friends)

# Subtraction
friends = friends - 2   # subtracting 2 friend
friends -= 2            # subtracting 2 friend
print(friends)

# Mulitplication
friends = friends * 3
friends *= -2
print(friends)

# Division
friends = friends / 2
friends /= 3
print(friends)

# Exponents
friends = friends ** 2
friends **= 2
print(friends)

# Modulas (Remainder)
remainder = friends % 9
remainder %= 3
print(remainder)